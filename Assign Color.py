def add_color_random(vertices_list):
    # Add a random color to each vertex in the input vertices list.
    output = []
    for v in vertices_list:
        color_buffer = []
        for i in range(0, 3):
            new_color = round(random(), ndigits=2)
            color_buffer.append(new_color)
        v_with_color = v + color_buffer
        output.append(v_with_color)
    return output


def print_output(vertices_list):
    # Given a vertices_list, print out a vertex (along with its color) for
    # each line.
    for v in vertices_list:
        output_string = ""
        for item in v:
            output_string += str(item) + ", "
        print(output_string)

